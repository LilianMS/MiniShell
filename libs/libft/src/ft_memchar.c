#include "../includes/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*src;
	unsigned char		uc;

	src = s;
	uc = c;
	while (n != 0)
	{
		if (*src == uc)
			return ((void *)src);
		src++;
		--n;
	}
	return (NULL);
}
