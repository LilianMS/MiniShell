#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*last;

	last = 0;
	while (*s != '\0')
	{
		if (*s == (unsigned char)c)
			last = s;
		s++;
	}
	if (c == '\0' || c == 1024)
		return ((char *)s);
	return ((char *)last);
}
